<template>
  <div class="home">
    <h1>On Broadway...</h1>

    <div class="shows">
      <div v-for="show in shows" :key="show.id" class="show">
        <img :src="show.poster" :alt="show.title">
        <h2>{{ show.title }}</h2>
      </div>
    </div>
  </div>
</template>

<script>
import firebase from "firebase";

export default {
  name: "home",
  data() {
    return {
      shows: []
    };
  },
  created() {
    this.fetchShows();
  },
  methods: {
    async fetchShows() {
      const query = await firebase
        .firestore()
        .collection("shows")
        .get();
      this.shows = query.docs.map(snapshot => {
        return {
          id: snapshot.id,
          ...snapshot.data()
        };
      });
    }
  }
};
</script>
